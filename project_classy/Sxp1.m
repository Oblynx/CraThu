%%% Sample Experiment %%%
clear;
runtimes=1;
import simulation.*;
%% World {N, T, Bounds, trailnum, (palette), v0, CaptureRadius, WaypointCap, framerate
wpars={...
	2,...
	300,...
	100,...
	5,...
	'grbcmykw',...
	3,...
	2,...
	5,...
	9035};
w=World(wpars);
%% Bots {x (pos), v (vel), c, strategy,  WP, wpc}
bpars1={...
	[1*w.Bounds*(-1-0.5),...
	1*w.Bounds*(1-0.5)],...
	3*[0.9980, -0.0628],...
	0.9,...
	'Evader1(bots, 1, world)',...
	[50 50; -50 50; -50 -50; 50 -50],...
	[1 0 0 0]};
bpars2={...
	[1*w.Bounds*(1-0.5),...
	1*w.Bounds*(-1-0.5)],...
	3*[-1, 0],...
	0.4,...
	'Pursuer1(bots, 2, world)',...
	[50 50; -50 50; -50 -50; 50 -50],...
	[1 0 0 0]};
bots=Bot(0, bpars1, bpars2);

%% Run simulation
WorldUpdate(w, bots);
h=History( w );
plotter=Plotter( w, length(bots(1).x), 1 );
runID=0;
for run=1:runtimes
	h=CSimulator( w, bots, h, plotter );
	filename= ['logs\history', runID,'.mat'];
	History.SaveHistory( h, filename );
	runID=runID+1;
end
